const express = require("express");
const router = express.Router();
const pool = require("../config/pool"); // Conex√£o com banco MySQL

// üîì GET /api/public/servicos ‚Äî Lista de colaboradores com suas especialidades (p√∫blico)
router.get("/", async (req, res) => {
  try {
    const [rows] = await pool.query(`
      SELECT 
        c.id,                    -- ID do colaborador
        c.nome,                  -- Nome do colaborador
        c.cargo,                 -- Cargo/fun√ß√£o
        c.whatsapp,              -- Contato para WhatsApp
        c.imagem,                -- Foto ou imagem do colaborador
        c.descricao,             -- Descri√ß√£o dos servi√ßos que ele faz
        e.nome AS especialidade_nome -- Nome da especialidade associada
      FROM colaboradores c
      LEFT JOIN especialidades e ON c.especialidade_id = e.id
    `);
    res.json(rows); // Retorna a lista de servi√ßos p√∫blicos
  } catch (err) {
    console.error("Erro ao buscar servi√ßos p√∫blicos:", err);
    res.status(500).json({ message: "Erro ao buscar servi√ßos." });
  }
});

module.exports = router;
